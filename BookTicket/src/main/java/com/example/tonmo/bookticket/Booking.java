package com.example.tonmo.bookticket;


public class Booking {

    private String mEmil,mCompany,mFrom,mTo,mBookedSeatAmount,mFare,mTime,mDate;

    public Booking(){

    }

    public String getmBookedSeatAmount() {
        return mBookedSeatAmount;
    }

    public void setmBookedSeatAmount(String mBookedSeatAmount) {
        this.mBookedSeatAmount = mBookedSeatAmount;
    }

    public String getmCompany() {
        return mCompany;
    }

    public void setmCompany(String mCompany) {
        this.mCompany = mCompany;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }

    public String getmEmil() {
        return mEmil;
    }

    public void setmEmil(String mEmil) {
        this.mEmil = mEmil;
    }

    public String getmFare() {
        return mFare;
    }

    public void setmFare(String mFare) {
        this.mFare = mFare;
    }

    public String getmFrom() {
        return mFrom;
    }

    public void setmFrom(String mFrom) {
        this.mFrom = mFrom;
    }

    public String getmTime() {
        return mTime;
    }

    public void setmTime(String mTime) {
        this.mTime = mTime;
    }

    public String getmTo() {
        return mTo;
    }

    public void setmTo(String mTo) {
        this.mTo = mTo;
    }
}
